        // TODO add your handling code here:

        //seleciona uma coluna da grid/jtable          
        int row = jTableCarrinho.getSelectedRow();

        if (row > -1) {
            IProduto item = carrinho.getItens().get(row);
            carrinho.removerItem(item);

            DefaultTableModel model = (DefaultTableModel) jTableCarrinho.getModel();
            model.removeRow(jTableCarrinho.getSelectedRow());
            ValorTotal();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para remover");
        }
